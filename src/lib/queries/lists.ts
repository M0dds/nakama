/** The five real list categories (1:1 with item types, minus `music`). A list
 *  can carry one of these OR none — null is "Alle" (uncategorized), the default
 *  state where the list lives in "Meine Listen" and the AddSheet imposes no
 *  type restriction. A non-null category is the primary grouping axis on /lists
 *  and locks the AddSheet's type filter to it. */
export type ListCategory = "anime" | "manga" | "series" | "movie" | "game";

/** Fixed display order + labels for the category sections on /lists and the
 *  category picker. Section names read as the plural-ish category, not the
 *  singular `typeLabel` (Serie → Serien, Film → Filme, Spiel → Spiele). */
export const LIST_CATEGORIES: { value: ListCategory; label: string }[] = [
  { value: "anime", label: "Anime" },
  { value: "manga", label: "Manga" },
  { value: "series", label: "Serien" },
  { value: "movie", label: "Filme" },
  { value: "game", label: "Spiele" },
];

/** Display label for a category — shared by the detail switcher, the AddSheet
 *  lock ("Nur <Kategorie>") and the move dialog's mismatch toast. */
export function listCategoryLabel(cat: ListCategory): string {
  return LIST_CATEGORIES.find((c) => c.value === cat)?.label ?? cat;
}

export interface ListSummary {
  /** UUID — used for RPC calls (delete, rename, set-tracks-home). */
  id: string;
  /** URL-stable identifier — `adj-adj-noun`, generated by the DB trigger
   *  `lists_set_short_code_trigger` on insert. Used in `/lists/<shortCode>`
   *  and in TanStack-Query keys. */
  shortCode: string;
  name: string;
  description: string | null;
  /** The list's media category, or null for "Alle" (uncategorized). Set by the
   *  owner only; members read it. Drives sectioning + AddSheet type-lock. */
  category: ListCategory | null;
  isShared: boolean;
  /** Caller's per-user tracks_home flag. Off → archive (lists I created but
   *  don't want to surface on Home / Calendar). */
  tracksHome: boolean;
  ownerId: string;
  createdAt: string;
  /** Owner-uploaded custom cover (public storage URL). Null → render the
   *  generated GeneratedCover from coverSeed instead. */
  coverUrl: string | null;
  /** Deterministic seed for the generated default cover (random theme +
   *  pattern). Assigned by the DB default on insert. */
  coverSeed: number;
  itemCount: number;
  memberCount: number;
  /** True when the caller is the list's creator. Used to gate the "Liste
   *  löschen" action (everyone else gets "Liste verlassen" once sharing
   *  lands). */
  isOwner: boolean;
  /** Caller's per-user pin state. Pinned lists float to the top of the
   *  caller's /lists view (independent of what other members see). */
  pinned: boolean;
  /** Caller's per-user manual sort_order. Higher pin / lower sort_order =
   *  closer to the top within its pin section. */
  sortOrder: number;
  /** Total released-but-unwatched EPISODES/chapters across the list in the
   *  last NEW_WINDOW_DAYS — drives the "Neue Folge"/"Neue Folgen" badge on
   *  /lists rows (count is summed per episode, not per item, so a same-day
   *  batch release reads as plural). Only shown for tracked lists. Manga have
   *  no air-date signal, so the kapitel side stays 0 until that source lands
   *  (Welle-2). */
  newCounts: { folgen: number; kapitel: number };
}

export interface ListEntry {
  listItemId: string;
  itemId: string;
  /** (type, slug) is the natural key used in `/item/:type/:slug`. */
  type: string;
  slug: string;
  title: string;
  coverUrl: string | null;
  syncEnabled: boolean;
  /** Shared per-list pin state. Pinning an item is visible to every member
   *  of the list, unlike the per-user list pin above. */
  pinned: boolean;
  sortOrder: number;
  /** True when at least one episode of this item aired within the last
   *  NEW_WINDOW_DAYS and the caller hasn't ticked it yet. Drives the
   *  "Neue Folge" badge in the list-detail row. Per-user — co-members
   *  evaluate the same item independently against their own watches. */
  hasNewEpisode: boolean;
  /** How many such episodes — drives singular vs plural wording on the badge
   *  ("Neue Folge" vs "Neue Folgen"). 0 when hasNewEpisode is false. */
  newEpisodeCount: number;
}


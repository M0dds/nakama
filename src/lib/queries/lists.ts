/**
 * Lists data layer — typed query options + mutation functions. Components
 * call these via createQuery / createMutation from @tanstack/solid-query.
 *
 * Naming convention:
 *   - listsQueryKey()             — overview (all visible lists)
 *   - listQueryKey(id)            — single list detail
 *   - listItemsQueryKey(id)       — items inside one list
 *
 * Mutations invalidate the keys they touch. Realtime subscriptions (added in
 * the chunk after this) call queryClient.invalidateQueries on the same keys
 * so a partner's write flows to your screen the same way your own does —
 * one source of truth for "this data changed", whether the trigger was local
 * or remote.
 *
 * RLS does the visibility filtering on every query — we never pass user_id
 * filters explicitly; lists_select_member + list_items_select_member +
 * list_members_select_co already scope the rows correctly.
 */

// ──────────────────────────────────────────────────────────────────────────
// Types
// ──────────────────────────────────────────────────────────────────────────

export interface ListSummary {
  /** UUID — used for RPC calls (delete, rename, set-tracks-home). */
  id: string;
  /** URL-stable identifier — `adj-adj-noun`, generated by the DB trigger
   *  `lists_set_short_code_trigger` on insert. Used in `/lists/<shortCode>`
   *  and in TanStack-Query keys. */
  shortCode: string;
  name: string;
  description: string | null;
  isShared: boolean;
  /** Caller's per-user tracks_home flag. Off → archive (lists I created but
   *  don't want to surface on Home / Calendar). */
  tracksHome: boolean;
  ownerId: string;
  createdAt: string;
  itemCount: number;
  memberCount: number;
  /** True when the caller is the list's creator. Used to gate the "Liste
   *  löschen" action (everyone else gets "Liste verlassen" once sharing
   *  lands). */
  isOwner: boolean;
  /** Caller's per-user pin state. Pinned lists float to the top of the
   *  caller's /lists view (independent of what other members see). */
  pinned: boolean;
  /** Caller's per-user manual sort_order. Higher pin / lower sort_order =
   *  closer to the top within its pin section. */
  sortOrder: number;
  /** Counts of items with released-but-unwatched episodes/chapters in the
   *  last NEW_WINDOW_DAYS — drives the "Neue Folge" / "N neue Folgen" badge
   *  on /lists rows. Manga have no air-date signal, so the kapitel side
   *  stays 0 until that source lands (Welle-2). */
  newCounts: { folgen: number; kapitel: number };
}

export interface ListEntry {
  listItemId: string;
  itemId: string;
  /** (type, slug) is the natural key used in `/item/:type/:slug`. */
  type: string;
  slug: string;
  title: string;
  coverUrl: string | null;
  syncEnabled: boolean;
  /** Shared per-list pin state. Pinning an item is visible to every member
   *  of the list, unlike the per-user list pin above. */
  pinned: boolean;
  sortOrder: number;
  /** True when at least one episode of this item aired within the last
   *  NEW_WINDOW_DAYS and the caller hasn't ticked it yet. Drives the
   *  "Neue Folge" badge in the list-detail row. Per-user — co-members
   *  evaluate the same item independently against their own watches. */
  hasNewEpisode: boolean;
}


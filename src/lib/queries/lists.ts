/** Delete a list. RLS (lists_delete_owner) restricts to the creator; the
 *  cascade on list_items / list_members / list_invitations cleans the rest.
 *
 *  `.select()` + null-check defends against a UI race where the caller
 *  loses ownership between render-time and click — e.g. an in-flight
 *  ownership transfer (Phase 7+). Without the check the optimistic UI
 *  would happily remove a list that's still on the server. */
export async function deleteList(listId: string): Promise<void> {
  const { data, error } = await supabase
    .from("lists")
    .delete()
    .eq("id", listId)
    .select("id")
    .maybeSingle();
  if (error) throw error;
  if (data === null)
    throw new Error("Liste konnte nicht gelöscht werden.");
}

/** Remove an item from a list (delete the list_items row). The item itself
 *  stays in `items` — it's still in the catalogue, just no longer on this
 *  list. Watch progress is keyed on items.id, so it's preserved even if the
 *  same item gets re-added later.
 *
 *  `.select()` after the delete so a silent RLS block (0 rows, no error)
 *  surfaces as a thrown error instead of the optimistic UI claiming success
 *  while the row stays in the table. */
export async function removeListItem(listItemId: string): Promise<void> {
  const { data, error } = await supabase
    .from("list_items")
    .delete()
    .eq("id", listItemId)
    .select("id")
    .maybeSingle();
  if (error) throw error;
  if (data === null)
    throw new Error("Eintrag konnte nicht entfernt werden.");
}

/** Move a list_items row to a different list. Sync is reset to false because
 *  the new list may have different members — if the caller wants sync in the
 *  new list, they re-enable it there (handshake §Phase 7).
 *
 *  CRITICAL: if the item is currently synced, we must run the REAL un-sync
 *  (`unsync_item`) BEFORE the move — not just flip `sync_enabled=false`. A bare
 *  flip strands data: the mover's progress lives in the instance lane
 *  (`list_item_id = LI`), so after the move the global-lane reads show the item
 *  as unwatched; and co-members' instance watch rows would orphan onto a
 *  list_item that now lives in a (possibly private) different list — a stale,
 *  privacy-leaky residue that re-surfaced as the "ghost co-watcher eye" bug.
 *  `unsync_item` unions every member's instance progress back into their own
 *  global lane (never loses progress), tears the instance down, and sets
 *  sync_enabled=false. We call it while the row is STILL in the source (shared)
 *  list so the member-scoped merge is correct.
 *
 *  `.select()` + null-check same rationale as removeListItem above. */
export async function moveListItem(input: {
  listItemId: string;
  targetListId: string;
}): Promise<void> {
  const { data: li, error: liErr } = await supabase
    .from("list_items")
    .select("sync_enabled")
    .eq("id", input.listItemId)
    .maybeSingle();
  if (liErr) throw liErr;
  if (li === null)
    throw new Error("Eintrag konnte nicht verschoben werden.");

  if (li.sync_enabled) {
    const { error: unsyncErr } = await supabase.rpc("unsync_item", {
      _list_item_id: input.listItemId,
    });
    if (unsyncErr) throw unsyncErr;
  }

  const { data, error } = await supabase
    .from("list_items")
    .update({ list_id: input.targetListId, sync_enabled: false })
    .eq("id", input.listItemId)
    .select("id")
    .maybeSingle();
  if (error) throw error;
  if (data === null)
    throw new Error("Eintrag konnte nicht verschoben werden.");
}


export async function createList(
  user: User,
  input: { name: string; description?: string },
): Promise<ListSummary> {
  const name = input.name.trim();
  if (!name) throw new Error("Name darf nicht leer sein.");

  const { data, error } = await supabase
    .from("lists")
    .insert({
      owner_id: user.id,
      name,
      description: input.description?.trim() || null,
    })
    .select(LIST_SELECT)
    .single();

  if (error) throw error;
  // The on_list_created trigger added the owner to list_members with
  // tracks_home=true automatically. The new list_members row gets
  // pinned_at=null and a sort_order assigned by the BEFORE-INSERT trigger
  // (MIN(scope)-1 → top of unpinned section). We don't read those back
  // here — the next invalidation refresh picks them up. For the optimistic
  // returnee we use safe defaults.
  return {
    ...toSummary(data as unknown as RawListRow, user, {
      tracksHome: true,
      pinned: false,
      sortOrder: 0,
    }),
    // A fresh list has no items yet — definitely no new episodes.
    newCounts: { ...EMPTY_COUNTS },
  };
}

/**
 * Rename. RLS (lists_update_member) lets any member rename. We `.select()`
 * the persisted name back so an RLS-blocked write (0 rows, no error)
 * surfaces as a returned `name: null` — components compare to detect the
 * silent block.
 */
export async function renameList(input: {
  listId: string;
  name: string;
}): Promise<{ name: string | null }> {
  const next = input.name.trim();
  if (!next) return { name: null };

  const { data, error } = await supabase
    .from("lists")
    .update({ name: next })
    .eq("id", input.listId)
    .select("name")
    .maybeSingle();
  if (error) throw error;
  return { name: (data as { name: string } | null)?.name ?? null };
}

/** Delete a list. RLS (lists_delete_owner) restricts to the creator; the
 *  cascade on list_items / list_members / list_invitations cleans the rest. */
export async function deleteList(listId: string): Promise<void> {
  const { error } = await supabase.from("lists").delete().eq("id", listId);
  if (error) throw error;
}

/** Remove an item from a list (delete the list_items row). The item itself
 *  stays in `items` — it's still in the catalogue, just no longer on this
 *  list. Watch progress is keyed on items.id, so it's preserved even if the
 *  same item gets re-added later.
 *
 *  `.select()` after the delete so a silent RLS block (0 rows, no error)
 *  surfaces as a thrown error instead of the optimistic UI claiming success
 *  while the row stays in the table. */
export async function removeListItem(listItemId: string): Promise<void> {
  const { data, error } = await supabase
    .from("list_items")
    .delete()
    .eq("id", listItemId)
    .select("id")
    .maybeSingle();
  if (error) throw error;
  if (data === null)
    throw new Error("Eintrag konnte nicht entfernt werden.");
}

/** Move a list_items row to a different list. Sync is reset to false because
 *  the new list may have different members — if the caller wants sync in the
 *  new list, they re-enable it there (handshake §Phase 7).
 *
 *  `.select()` + null-check same rationale as removeListItem above. */
export async function moveListItem(input: {
  listItemId: string;
  targetListId: string;
}): Promise<void> {
  const { data, error } = await supabase
    .from("list_items")
    .update({ list_id: input.targetListId, sync_enabled: false })
    .eq("id", input.listItemId)
    .select("id")
    .maybeSingle();
  if (error) throw error;
  if (data === null)
    throw new Error("Eintrag konnte nicht verschoben werden.");
}

/**
 * Toggle the per-user tracks_home flag. Off = archive mode for the caller
 * only — items in this list drop off Home / Calendar / Logbook for them.
 * Other members keep their own setting.
 *
 * `.select()` to detect silent RLS blocks (0 rows updated, no error).
 */
export async function setListTracking(
  user: User,
  input: { listId: string; enabled: boolean },
): Promise<{ tracksHome: boolean | null }> {
  const { data, error } = await supabase
    .from("list_members")
    .update({ tracks_home: input.enabled })
    .eq("list_id", input.listId)
    .eq("user_id", user.id)
    .select("tracks_home")
    .maybeSingle();
  if (error) throw error;
  return {
    tracksHome:
      (data as { tracks_home: boolean } | null)?.tracks_home ?? null,
  };
}

/**
 * Toggle the per-user list pin on `list_members`. Also assigns a fresh
 * sort_order so the pinned (or freshly-unpinned) list lands at the TOP of
 * its new section — `MIN(section.sort_order) - 1`. The caller computes
 * `sortOrder` from the current query cache so we don't need an extra
 * round-trip. Returns the post-write pinned state so the caller can
 * detect silent RLS blocks (0 rows → null).
 */
export async function setListPin(input: {
  listId: string;
  userId: string;
  pinned: boolean;
  sortOrder: number;
}): Promise<{ pinned: boolean | null }> {
  const { data, error } = await supabase
    .from("list_members")
    .update({
      pinned_at: input.pinned ? new Date().toISOString() : null,
      sort_order: input.sortOrder,
    })
    .eq("list_id", input.listId)
    .eq("user_id", input.userId)
    .select("pinned_at")
    .maybeSingle();
  if (error) throw error;
  return {
    pinned: data ? (data as { pinned_at: string | null }).pinned_at !== null : null,
  };
}

/**
 * Toggle the shared per-list pin on `list_items`. Same sortOrder-bump
 * pattern as setListPin so the pinned/unpinned item floats to the top of
 * its new section.
 */
export async function setListItemPin(input: {
  listItemId: string;
  pinned: boolean;
  sortOrder: number;
}): Promise<{ pinned: boolean | null }> {
  const { data, error } = await supabase
    .from("list_items")
    .update({
      pinned_at: input.pinned ? new Date().toISOString() : null,
      sort_order: input.sortOrder,
    })
    .eq("id", input.listItemId)
    .select("pinned_at")
    .maybeSingle();
  if (error) throw error;
  return {
    pinned: data ? (data as { pinned_at: string | null }).pinned_at !== null : null,
  };
}

/**
 * Atomic reorder of the caller's list_members rows for one pin section.
 * Goes through the `reorder_list_members` RPC: a single UPDATE statement
 * server-side, so every realtime subscriber sees a consistent post-write
 * state. The previous parallel-UPDATE implementation fired N events at
 * different times, and a refetch landing between them would read a
 * partial state — that's the "hover bg flickers 2-3 times after drop"
 * the user was hitting.
 */
export async function reorderLists(input: {
  userId: string;
  orderedListIds: string[];
}): Promise<void> {
  const { error } = await supabase.rpc("reorder_list_members", {
    _user_id: input.userId,
    _ordered_list_ids: input.orderedListIds,
  });
  if (error) throw error;
}

/** Atomic reorder of one pin section of a list's list_items, via the
 *  `reorder_list_items` RPC. Same single-UPDATE rationale as reorderLists.
 *  `listId` is required so the server-side authorization check (caller
 *  must be a member of the list) has a target. */
export async function reorderListItems(input: {
  listId: string;
  orderedListItemIds: string[];
}): Promise<void> {
  const { error } = await supabase.rpc("reorder_list_items", {
    _list_id: input.listId,
    _ordered_list_item_ids: input.orderedListItemIds,
  });
  if (error) throw error;
}

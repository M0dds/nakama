/** PostgREST embed shape — list_items(count) and list_members(count) return
 *  as a single-element array of { count }. */
interface RawListRow {
  id: string;
  short_code: string;
  name: string;
  description: string | null;
  category: string | null;
  is_shared: boolean;
  owner_id: string;
  created_at: string;
  cover_url: string | null;
  cover_seed: number;
  list_items: { count: number }[] | null;
  list_members: { count: number }[] | null;
}

const LIST_SELECT =
  "id, short_code, name, description, category, is_shared, owner_id, created_at, cover_url, cover_seed, list_items(count), list_members(count)";

function toSummary(
  row: RawListRow,
  user: User,
  membership: { tracksHome: boolean; pinned: boolean; sortOrder: number },
): Omit<ListSummary, "newCounts"> {
  return {
    id: row.id,
    shortCode: row.short_code,
    name: row.name,
    description: row.description,
    category: (row.category as ListCategory | null) ?? null,
    isShared: row.is_shared,
    tracksHome: membership.tracksHome,
    ownerId: row.owner_id,
    createdAt: row.created_at,
    coverUrl: row.cover_url,
    coverSeed: row.cover_seed,
    itemCount: embedCount(row.list_items),
    memberCount: embedCount(row.list_members),
    isOwner: row.owner_id === user.id,
    pinned: membership.pinned,
    sortOrder: membership.sortOrder,
  };
}

/** Empty new-counts default — used in places where we don't have the
 *  newItemsMap (single-list query, optimistic createList return). */
const EMPTY_COUNTS = { folgen: 0, kapitel: 0 } as const;

/** Pinned-first, then by sort_order ASC within each section. Server can't
 *  express "group by (pinned_at IS NULL) then sort by sort_order" without a
 *  generated column or RPC, so we group client-side. With < 100 lists this
 *  is free. */
function bySectionThenSort(a: ListSummary, b: ListSummary): number {
  if (a.pinned !== b.pinned) return a.pinned ? -1 : 1;
  return a.sortOrder - b.sortOrder;
}

/** Window matching the Logbuch's "missed" definition — a released episode
 *  counts as "new" if it aired in the last NEW_WINDOW_DAYS and the user
 *  hasn't ticked it. Keeps the lists badge and the home modules in sync. */
const NEW_WINDOW_DAYS = 14;

/** One list_item candidate for the new-episode check — carries its item plus
 *  the lane selector (sync-instances model). */
interface NewEpisodeEntry {
  itemId: string;
  listItemId: string;
  /** Synced → read this list_item's INSTANCE rows; else the caller's GLOBAL
   *  rows. */
  syncEnabled: boolean;
  /** Group-shared Anzeige-Tag of this synced instance (list_items.display_weekday).
   *  Ignored for non-synced entries (those take the per-user global override). */
  instanceWeekday: number | null;
}

/** Shared empty set for synced entries that have no instance rows yet — never
 *  mutated, so it's safe to alias. */
const EMPTY_EPISODE_SET: ReadonlySet<string> = new Set();

/**
 * Maps each LIST_ITEM that has released-but-unwatched episodes inside the
 * new-window to HOW MANY such episodes it has, reading EACH entry against its
 * correct progress lane (sync-instances model):
 *   - non-synced entry → the caller's GLOBAL watches (`list_item_id IS NULL`)
 *   - synced entry      → that list_item's INSTANCE watches (`list_item_id = LI`)
 * Keyed on listItemId (not itemId) because the same item can appear as both a
 * global and a synced entry across different lists, with different badges.
 * Entries with zero new episodes are absent from the map (so `map.has(id)` /
 * `(map.get(id) ?? 0) > 0` is the "has new" test). The count drives singular-
 * vs-plural wording ("Neue Folge" vs "Neue Folgen") — a single same-day batch
 * release is several new episodes on ONE item, which an item-count would miss.
 *
 * Round-trips: episodes-in-window, the caller's global watches over them, and
 * — only when some entry is synced — the instance watches in one bulk query.
 * The candidate set is passed in scoped (one list, or all visible lists), so we
 * never scan the user's full catalogue.
 */
async function findItemsWithNewEpisodes(
  userId: string,
  entries: NewEpisodeEntry[],
): Promise<Map<string, number>> {
  if (entries.length === 0) return new Map();

  const itemIds = unique(entries.map((e) => e.itemId));
  const now = Date.now();
  const sinceIso = new Date(now - NEW_WINDOW_DAYS * 86_400_000).toISOString();
  const nowIso = new Date(now).toISOString();
  const { data: epRows, error: epErr } = await supabase
    .from("episodes")
    .select("id, item_id, air_date")
    .in("item_id", itemIds)
    .gte("air_date", sinceIso)
    .lte("air_date", nowIso);
  if (epErr) {
    console.error("episodes window query failed", epErr);
    return new Map();
  }
  const eps = (epRows ?? []) as {
    id: string;
    item_id: string;
    air_date: string | null;
  }[];
  if (eps.length === 0) return new Map();
  const epIds = eps.map((e) => e.id);

  // Recent episodes (id + air_date) grouped by item, so each entry tests its
  // own item against its own lane's Anzeige-Tag.
  const epsByItem = new Map<string, { id: string; airDate: string | null }[]>();
  for (const e of eps) {
    const arr = epsByItem.get(e.item_id);
    const ep = { id: e.id, airDate: e.air_date };
    if (arr) arr.push(ep);
    else epsByItem.set(e.item_id, [ep]);
  }

  // Per-user global Anzeige-Tag overrides (non-synced entries snap by these;
  // synced entries use their instance's group-shared weekday). One small read.
  const { data: prefRows } = await supabase
    .from("item_display_prefs")
    .select("item_id, weekday")
    .eq("user_id", userId);
  const globalWeekday = new Map<string, number>();
  for (const p of prefRows ?? [])
    globalWeekday.set(p.item_id as string, p.weekday as number);

  // Global watched set (list_item_id IS NULL) — covers every non-synced entry.
  const { data: gRows, error: gErr } = await supabase
    .from("episode_watches")
    .select("episode_id")
    .eq("user_id", userId)
    .is("list_item_id", null)
    .in("episode_id", epIds);
  if (gErr) {
    console.error("episode_watches global join failed", gErr);
    return new Map();
  }
  const globalWatched = new Set((gRows ?? []).map((w) => w.episode_id as string));

  // Instance watched sets, grouped by list_item_id — one bulk query over all
  // synced list_items, fetched only when at least one entry is synced.
  const syncedIds = unique(
    entries.filter((e) => e.syncEnabled).map((e) => e.listItemId),
  );
  const instanceWatched = new Map<string, Set<string>>();
  if (syncedIds.length > 0) {
    const { data: iRows, error: iErr } = await supabase
      .from("episode_watches")
      .select("episode_id, list_item_id")
      .eq("user_id", userId)
      .in("list_item_id", syncedIds)
      .in("episode_id", epIds);
    if (iErr) {
      console.error("episode_watches instance join failed", iErr);
    } else {
      for (const r of iRows ?? []) {
        const li = r.list_item_id as string;
        let set = instanceWatched.get(li);
        if (!set) instanceWatched.set(li, (set = new Set()));
        set.add(r.episode_id as string);
      }
    }
  }

  const result = new Map<string, number>();
  for (const entry of entries) {
    const itemEps = epsByItem.get(entry.itemId);
    if (!itemEps) continue;
    const watched = entry.syncEnabled
      ? instanceWatched.get(entry.listItemId) ?? EMPTY_EPISODE_SET
      : globalWatched;
    // Anzeige-Tag for this lane: synced → the instance's group value, else the
    // viewer's global override. An episode only counts as "new" once its SNAPPED
    // availability day has arrived — a Mon-aired episode with a Fri override
    // surfaces as a badge on Friday, not before. (Snap moves ≥0 days forward, so
    // the fetched [now-14d, now] window already covers every snapped result.)
    const weekday = entry.syncEnabled
      ? entry.instanceWeekday
      : globalWeekday.get(entry.itemId) ?? null;
    const unwatched = itemEps.reduce((n, ep) => {
      if (watched.has(ep.id)) return n;
      if (!ep.airDate) return n + 1;
      return new Date(snapToWeekday(ep.airDate, weekday)).getTime() <= now
        ? n + 1
        : n;
    }, 0);
    if (unwatched > 0) result.set(entry.listItemId, unwatched);
  }
  return result;
}

/** Aggregate (list_id, listItemId, type) entries into per-list (folgen,
 *  kapitel) counts — summing the NEW-EPISODE COUNT per qualifying list_item,
 *  not just the item tally, so the singular/plural wording reflects "more than
 *  one new episode" even when they sit on a single item (a same-day batch
 *  release). anime/series count as Folgen, manga as Kapitel; movies/games are
 *  ignored (no episode model). Type + listItemId travel alongside so we don't
 *  need a separate items lookup, and the lookup keys on listItemId (sync-aware). */
function aggregateNewCounts(
  pairs: { list_id: string; listItemId: string; type: string }[],
  newEpisodeCounts: Map<string, number>,
): Map<string, { folgen: number; kapitel: number }> {
  const counts = new Map<string, { folgen: number; kapitel: number }>();
  for (const p of pairs) {
    const n = newEpisodeCounts.get(p.listItemId) ?? 0;
    if (n === 0) continue;
    const c = counts.get(p.list_id) ?? { folgen: 0, kapitel: 0 };
    if (p.type === "manga") c.kapitel += n;
    else if (p.type === "anime" || p.type === "series") c.folgen += n;
    counts.set(p.list_id, c);
  }
  return counts;
}

/**
 * All lists visible to the current user (RLS scopes via lists_select_member).
 * Split into private / shared so the UI can render the two groups; the split
 * is by is_shared, not ownership — a list you created becomes "geteilt" the
 * moment you invite someone (Phase 7+).
 *
 * Two parallel queries: lists+embedded counts, and the caller's own
 * list_members rows for tracks_home. The original Logbook also fetched
 * "newCounts" for episode badges — deferred until items + episodes land
 * in Phases 4–5.
 */
interface RawMembershipJoinRow {
  pinned_at: string | null;
  sort_order: number;
  tracks_home: boolean;
  lists: RawListRow;
}

export function listsQueryOptions(user: User) {
  return {
    queryKey: listsQueryKey,
    queryFn: async (): Promise<{
      private: ListSummary[];
      shared: ListSummary[];
    }> => {
      // Two parallel queries: the lists+memberships join, and the items-
      // on-visible-lists pairs (with type for the folgen/kapitel split).
      // The pairs query subsumes both the "what items can the user see"
      // and the "what type is each" lookups that the previous helper did
      // in separate round-trips. RLS scopes the pairs to lists the user
      // is a member of.
      const [membershipRes, pairsRes] = await Promise.all([
        supabase
          .from("list_members")
          .select(
            `pinned_at, sort_order, tracks_home, lists!inner(${LIST_SELECT})`,
          )
          .eq("user_id", user.id),
        supabase
          .from("list_items")
          .select(
            "id, sync_enabled, display_weekday, list_id, item_id, items!inner(type)",
          )
          // Windowed by the user's total list_items (normally well under 1000).
          // NOTE: Supabase's hard 1000-row cap (db-max-rows) overrides this
          // `.limit` — a user with 1000+ list_items across all lists would
          // under-count badges past 1000 (no error, just a missing badge).
          // Acceptable at this app's scale; paginate with `.range()` if it ever
          // bites. (Same cap class as the co-watcher eye / title-gap fixes.)
          .limit(5000),
      ]);

      if (membershipRes.error) throw membershipRes.error;
      if (pairsRes.error)
        console.error("list_items pair lookup failed", pairsRes.error);

      // Flatten PostgREST's embedded { items: { type } } shape. Skip rows
      // where the items embed came back null (RLS edge-case — shouldn't
      // happen since lists+items share caller-visible scope, but defensive).
      const rawPairs = (pairsRes.data ?? []) as unknown as Array<{
        id: string;
        sync_enabled: boolean;
        display_weekday: number | null;
        list_id: string;
        item_id: string;
        items: { type: string } | null;
      }>;
      const flatPairs = rawPairs.flatMap((r) => {
        const type = r.items?.type;
        if (!type) return [];
        return [
          {
            list_id: r.list_id,
            listItemId: r.id,
            item_id: r.item_id,
            type,
            syncEnabled: r.sync_enabled,
            instanceWeekday: r.display_weekday,
          },
        ];
      });

      // Episode-window check runs serial-after the pairs because it needs the
      // candidate set. Per-list_item (sync-aware) so a synced instance and a
      // global entry of the same item get independent badges; see
      // findItemsWithNewEpisodes.
      const newEpisodeCounts = await findItemsWithNewEpisodes(
        user.id,
        flatPairs.map((p) => ({
          itemId: p.item_id,
          listItemId: p.listItemId,
          syncEnabled: p.syncEnabled,
          instanceWeekday: p.instanceWeekday,
        })),
      );
      const newCountsByList = aggregateNewCounts(flatPairs, newEpisodeCounts);

      const rows = (membershipRes.data ?? []) as unknown as RawMembershipJoinRow[];
      const summaries: ListSummary[] = rows
        .map((r) => ({
          ...toSummary(r.lists, user, {
            tracksHome: r.tracks_home,
            pinned: r.pinned_at !== null,
            sortOrder: r.sort_order,
          }),
          newCounts: newCountsByList.get(r.lists.id) ?? { ...EMPTY_COUNTS },
        }))
        .sort(bySectionThenSort);

      return {
        private: summaries.filter((l) => !l.isShared),
        shared: summaries.filter((l) => l.isShared),
      };
    },
  };
}

/** A single list by short_code, or null if not visible (RLS) / unknown
 *  code. Membership lookup happens after we've got the list-id, because
 *  list_members is keyed on the UUID. */
export function listQueryOptions(user: User, shortCode: string) {
  return {
    queryKey: listQueryKey(shortCode),
    queryFn: async (): Promise<ListSummary | null> => {
      const listRes = await supabase
        .from("lists")
        .select(LIST_SELECT)
        .eq("short_code", shortCode)
        .maybeSingle();

      if (listRes.error) throw listRes.error;
      if (!listRes.data) return null;

      const raw = listRes.data as unknown as RawListRow;
      const membershipRes = await supabase
        .from("list_members")
        .select("tracks_home, pinned_at, sort_order")
        .eq("list_id", raw.id)
        .eq("user_id", user.id)
        .maybeSingle();

      const m = membershipRes.data as
        | { tracks_home: boolean; pinned_at: string | null; sort_order: number }
        | null;
      return {
        ...toSummary(raw, user, {
          tracksHome: m?.tracks_home ?? true,
          pinned: m?.pinned_at != null,
          sortOrder: m?.sort_order ?? 0,
        }),
        // The single-list detail view doesn't render the per-list badge —
        // it shows per-item badges instead. Skip the new-counts roundtrip.
        newCounts: { ...EMPTY_COUNTS },
      };
    },
  };
}

interface RawListItemRow {
  id: string;
  sync_enabled: boolean;
  display_weekday: number | null;
  item_id: string;
  pinned_at: string | null;
  sort_order: number;
  items: {
    title: string;
    type: string;
    slug: string;
    cover_url: string | null;
  } | null;
}

/** Items placed in a list, ordered pinned-first then by sort_order ASC,
 *  scoped by the list's short_code. PostgREST `lists!inner(short_code)`
 *  does the FK join + filter in one round-trip; RLS still scopes the rows
 *  to lists the caller can see. Pin grouping happens client-side (see
 *  `bySectionThenSort` rationale on the lists query above).
 *
 *  Each entry gets a per-user `hasNewEpisode` flag derived from the same
 *  findItemsWithNewEpisodes() helper that powers the overview badge,
 *  called here scoped to this list's items only. */
export function listItemsQueryOptions(user: User, shortCode: string) {
  return {
    queryKey: listItemsQueryKey(shortCode),
    queryFn: async (): Promise<ListEntry[]> => {
      // Scoped fetch: this list's list_items + the embedded items meta
      // (title/type/slug/cover_url). The itemIds extracted from the
      // response bound the new-episode check below — no need to scan the
      // user's entire catalogue the way the previous global helper did.
      const { data, error } = await supabase
        .from("list_items")
        .select(
          "id, sync_enabled, display_weekday, item_id, pinned_at, sort_order, lists!inner(short_code), items(title, type, slug, cover_url)",
        )
        .eq("lists.short_code", shortCode);
      if (error) throw error;

      const rawRows = (data as unknown as RawListItemRow[]) ?? [];
      const newEpisodeCounts = await findItemsWithNewEpisodes(
        user.id,
        rawRows.map((r) => ({
          itemId: r.item_id,
          listItemId: r.id,
          syncEnabled: r.sync_enabled,
          instanceWeekday: r.display_weekday,
        })),
      );

      const rows = rawRows.map((r) => {
        const count = newEpisodeCounts.get(r.id) ?? 0;
        return {
          listItemId: r.id,
          itemId: r.item_id,
          type: r.items?.type ?? "anime",
          slug: r.items?.slug ?? "",
          title: r.items?.title ?? "Unbekannt",
          coverUrl: r.items?.cover_url ?? null,
          syncEnabled: r.sync_enabled,
          pinned: r.pinned_at !== null,
          sortOrder: r.sort_order,
          hasNewEpisode: count > 0,
          newEpisodeCount: count,
        };
      });
      return rows.sort((a, b) => {
        if (a.pinned !== b.pinned) return a.pinned ? -1 : 1;
        return a.sortOrder - b.sortOrder;
      });
    },
  };
}


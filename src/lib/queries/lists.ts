/** PostgREST embed shape — list_items(count) and list_members(count) return
 *  as a single-element array of { count }. */
interface RawListRow {
  id: string;
  short_code: string;
  name: string;
  description: string | null;
  is_shared: boolean;
  owner_id: string;
  created_at: string;
  list_items: { count: number }[] | null;
  list_members: { count: number }[] | null;
}

const LIST_SELECT =
  "id, short_code, name, description, is_shared, owner_id, created_at, list_items(count), list_members(count)";

function toSummary(
  row: RawListRow,
  user: User,
  membership: { tracksHome: boolean; pinned: boolean; sortOrder: number },
): Omit<ListSummary, "newCounts"> {
  return {
    id: row.id,
    shortCode: row.short_code,
    name: row.name,
    description: row.description,
    isShared: row.is_shared,
    tracksHome: membership.tracksHome,
    ownerId: row.owner_id,
    createdAt: row.created_at,
    itemCount: embedCount(row.list_items),
    memberCount: embedCount(row.list_members),
    isOwner: row.owner_id === user.id,
    pinned: membership.pinned,
    sortOrder: membership.sortOrder,
  };
}

/** Empty new-counts default — used in places where we don't have the
 *  newItemsMap (single-list query, optimistic createList return). */
const EMPTY_COUNTS = { folgen: 0, kapitel: 0 } as const;

/** Pinned-first, then by sort_order ASC within each section. Server can't
 *  express "group by (pinned_at IS NULL) then sort by sort_order" without a
 *  generated column or RPC, so we group client-side. With < 100 lists this
 *  is free. */
function bySectionThenSort(a: ListSummary, b: ListSummary): number {
  if (a.pinned !== b.pinned) return a.pinned ? -1 : 1;
  return a.sortOrder - b.sortOrder;
}

/** Window matching the Logbuch's "missed" definition — a released episode
 *  counts as "new" if it aired in the last NEW_WINDOW_DAYS and the user
 *  hasn't ticked it. Keeps the lists badge and the home modules in sync. */
const NEW_WINDOW_DAYS = 14;

/**
 * Filters `itemIds` down to the subset that have at least one released-
 * but-unwatched episode inside the new-window. Two round-trips: episodes
 * in window, then the caller's watches over them.
 *
 * The candidate item set is passed in so the caller can scope it — e.g.
 * `listItemsQueryOptions` only cares about items in ONE list and no longer
 * needs to scan the user's full catalogue. Item-type resolution lives
 * with the caller (who already has it from their main fetch), saving the
 * previous fourth round-trip to `items`.
 */
async function findItemsWithNewEpisodes(
  userId: string,
  itemIds: string[],
): Promise<Set<string>> {
  if (itemIds.length === 0) return new Set();

  const sinceIso = new Date(
    Date.now() - NEW_WINDOW_DAYS * 86_400_000,
  ).toISOString();
  const nowIso = new Date().toISOString();
  const { data: epRows, error: epErr } = await supabase
    .from("episodes")
    .select("id, item_id")
    .in("item_id", itemIds)
    .gte("air_date", sinceIso)
    .lte("air_date", nowIso);
  if (epErr) {
    console.error("episodes window query failed", epErr);
    return new Set();
  }
  const eps = (epRows ?? []) as { id: string; item_id: string }[];
  if (eps.length === 0) return new Set();

  const { data: wRows, error: wErr } = await supabase
    .from("episode_watches")
    .select("episode_id")
    .in(
      "episode_id",
      eps.map((e) => e.id),
    )
    .eq("user_id", userId);
  if (wErr) {
    console.error("episode_watches join failed", wErr);
    return new Set();
  }
  const watched = new Set((wRows ?? []).map((w) => w.episode_id as string));

  const result = new Set<string>();
  for (const e of eps) if (!watched.has(e.id)) result.add(e.item_id);
  return result;
}

/** Aggregate (list_id, item_id, type) pairs into per-list (folgen, kapitel)
 *  counts, filtered to the items that qualified as new. anime/series count
 *  as Folgen, manga as Kapitel; movies/games are ignored (no episode model).
 *  Type travels alongside the pair so we don't need a separate items lookup. */
function aggregateNewCounts(
  pairs: { list_id: string; item_id: string; type: string }[],
  newItemsSet: Set<string>,
): Map<string, { folgen: number; kapitel: number }> {
  const counts = new Map<string, { folgen: number; kapitel: number }>();
  for (const p of pairs) {
    if (!newItemsSet.has(p.item_id)) continue;
    const c = counts.get(p.list_id) ?? { folgen: 0, kapitel: 0 };
    if (p.type === "manga") c.kapitel++;
    else if (p.type === "anime" || p.type === "series") c.folgen++;
    counts.set(p.list_id, c);
  }
  return counts;
}

/**
 * All lists visible to the current user (RLS scopes via lists_select_member).
 * Split into private / shared so the UI can render the two groups; the split
 * is by is_shared, not ownership — a list you created becomes "geteilt" the
 * moment you invite someone (Phase 7+).
 *
 * Two parallel queries: lists+embedded counts, and the caller's own
 * list_members rows for tracks_home. The original Logbook also fetched
 * "newCounts" for episode badges — deferred until items + episodes land
 * in Phases 4–5.
 */
interface RawMembershipJoinRow {
  pinned_at: string | null;
  sort_order: number;
  tracks_home: boolean;
  lists: RawListRow;
}

export function listsQueryOptions(user: User) {
  return {
    queryKey: listsQueryKey,
    queryFn: async (): Promise<{
      private: ListSummary[];
      shared: ListSummary[];
    }> => {
      // Two parallel queries: the lists+memberships join, and the items-
      // on-visible-lists pairs (with type for the folgen/kapitel split).
      // The pairs query subsumes both the "what items can the user see"
      // and the "what type is each" lookups that the previous helper did
      // in separate round-trips. RLS scopes the pairs to lists the user
      // is a member of.
      const [membershipRes, pairsRes] = await Promise.all([
        supabase
          .from("list_members")
          .select(
            `pinned_at, sort_order, tracks_home, lists!inner(${LIST_SELECT})`,
          )
          .eq("user_id", user.id),
        supabase
          .from("list_items")
          .select("list_id, item_id, items!inner(type)"),
      ]);

      if (membershipRes.error) throw membershipRes.error;
      if (pairsRes.error)
        console.error("list_items pair lookup failed", pairsRes.error);

      // Flatten PostgREST's embedded { items: { type } } shape. Skip rows
      // where the items embed came back null (RLS edge-case — shouldn't
      // happen since lists+items share caller-visible scope, but defensive).
      const rawPairs = (pairsRes.data ?? []) as unknown as Array<{
        list_id: string;
        item_id: string;
        items: { type: string } | null;
      }>;
      const flatPairs = rawPairs.flatMap((r) => {
        const type = r.items?.type;
        if (!type) return [];
        return [{ list_id: r.list_id, item_id: r.item_id, type }];
      });
      const itemIds = unique(flatPairs.map((p) => p.item_id));

      // Episode-window check runs serial-after the pairs because it needs
      // the candidate item set. Two more round-trips inside; see
      // findItemsWithNewEpisodes.
      const newItemsSet = await findItemsWithNewEpisodes(user.id, itemIds);
      const newCountsByList = aggregateNewCounts(flatPairs, newItemsSet);

      const rows = (membershipRes.data ?? []) as unknown as RawMembershipJoinRow[];
      const summaries: ListSummary[] = rows
        .map((r) => ({
          ...toSummary(r.lists, user, {
            tracksHome: r.tracks_home,
            pinned: r.pinned_at !== null,
            sortOrder: r.sort_order,
          }),
          newCounts: newCountsByList.get(r.lists.id) ?? { ...EMPTY_COUNTS },
        }))
        .sort(bySectionThenSort);

      return {
        private: summaries.filter((l) => !l.isShared),
        shared: summaries.filter((l) => l.isShared),
      };
    },
  };
}

/** A single list by short_code, or null if not visible (RLS) / unknown
 *  code. Membership lookup happens after we've got the list-id, because
 *  list_members is keyed on the UUID. */
export function listQueryOptions(user: User, shortCode: string) {
  return {
    queryKey: listQueryKey(shortCode),
    queryFn: async (): Promise<ListSummary | null> => {
      const listRes = await supabase
        .from("lists")
        .select(LIST_SELECT)
        .eq("short_code", shortCode)
        .maybeSingle();

      if (listRes.error) throw listRes.error;
      if (!listRes.data) return null;

      const raw = listRes.data as unknown as RawListRow;
      const membershipRes = await supabase
        .from("list_members")
        .select("tracks_home, pinned_at, sort_order")
        .eq("list_id", raw.id)
        .eq("user_id", user.id)
        .maybeSingle();

      const m = membershipRes.data as
        | { tracks_home: boolean; pinned_at: string | null; sort_order: number }
        | null;
      return {
        ...toSummary(raw, user, {
          tracksHome: m?.tracks_home ?? true,
          pinned: m?.pinned_at != null,
          sortOrder: m?.sort_order ?? 0,
        }),
        // The single-list detail view doesn't render the per-list badge —
        // it shows per-item badges instead. Skip the new-counts roundtrip.
        newCounts: { ...EMPTY_COUNTS },
      };
    },
  };
}

interface RawListItemRow {
  id: string;
  sync_enabled: boolean;
  item_id: string;
  pinned_at: string | null;
  sort_order: number;
  items: {
    title: string;
    type: string;
    slug: string;
    cover_url: string | null;
  } | null;
}

/** Items placed in a list, ordered pinned-first then by sort_order ASC,
 *  scoped by the list's short_code. PostgREST `lists!inner(short_code)`
 *  does the FK join + filter in one round-trip; RLS still scopes the rows
 *  to lists the caller can see. Pin grouping happens client-side (see
 *  `bySectionThenSort` rationale on the lists query above).
 *
 *  Each entry gets a per-user `hasNewEpisode` flag derived from the same
 *  findItemsWithNewEpisodes() helper that powers the overview badge,
 *  called here scoped to this list's items only. */
export function listItemsQueryOptions(user: User, shortCode: string) {
  return {
    queryKey: listItemsQueryKey(shortCode),
    queryFn: async (): Promise<ListEntry[]> => {
      // Scoped fetch: this list's list_items + the embedded items meta
      // (title/type/slug/cover_url). The itemIds extracted from the
      // response bound the new-episode check below — no need to scan the
      // user's entire catalogue the way the previous global helper did.
      const { data, error } = await supabase
        .from("list_items")
        .select(
          "id, sync_enabled, item_id, pinned_at, sort_order, lists!inner(short_code), items(title, type, slug, cover_url)",
        )
        .eq("lists.short_code", shortCode);
      if (error) throw error;

      const rawRows = (data as unknown as RawListItemRow[]) ?? [];
      const itemIds = unique(rawRows.map((r) => r.item_id));
      const newItemsSet = await findItemsWithNewEpisodes(user.id, itemIds);

      const rows = rawRows.map((r) => ({
        listItemId: r.id,
        itemId: r.item_id,
        type: r.items?.type ?? "anime",
        slug: r.items?.slug ?? "",
        title: r.items?.title ?? "Unbekannt",
        coverUrl: r.items?.cover_url ?? null,
        syncEnabled: r.sync_enabled,
        pinned: r.pinned_at !== null,
        sortOrder: r.sort_order,
        hasNewEpisode: newItemsSet.has(r.item_id),
      }));
      return rows.sort((a, b) => {
        if (a.pinned !== b.pinned) return a.pinned ? -1 : 1;
        return a.sortOrder - b.sortOrder;
      });
    },
  };
}

